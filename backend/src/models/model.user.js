const mongoose = require('mongoose');

// creating schema for user registration
const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    require: true,
  },
  lastName: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
    unique: true,
  },
  password: {
    type: String,
    require: true
  }
});

module.exports = mongoose.model("User", userSchema);